package sef.FinalActivity;

public class Calculator {

    public int fAdd(int a, int b) {

        return a + b;
    }

    public int fSubtract(int a, int b){
        return a - b;
    }

    public double fMultiply(double a, double b){
        return a * b;
    }

    public double fDivide(double a, double b){
        return a / b;
    }
}
